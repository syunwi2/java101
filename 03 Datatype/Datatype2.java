
public class Datatype2 {

	public static void main(String[] args) {
		System.out.println("minimun value of byte : " + Byte.MIN_VALUE);		// -128
		System.out.println("maximun value of byte : " + Byte.MAX_VALUE);		// 127
		
		System.out.println("minimun value of short : " + Short.MIN_VALUE);		// -32768
		System.out.println("maximun value of short : " + Short.MAX_VALUE);		// 32767
		
		System.out.println("minimun value of int : " + Integer.MIN_VALUE);		// -2147483648
		System.out.println("maximun value of int : " + Integer.MAX_VALUE);		// 2147483647
		
		System.out.println("minimun value of long : " + Long.MIN_VALUE);		// -9223372036854775808
		System.out.println("maximun value of long : " + Long.MAX_VALUE);		// 9223372036854775807
		
		System.out.println("minimun value of float : " + Float.MIN_VALUE);		// 1.4E-45
		System.out.println("maximun value of float : " + Float.MAX_VALUE);		// 3.4028235E38
		
		System.out.println("minimun value of double : " + Double.MIN_VALUE);	// 4.9E-324
		System.out.println("maximun value of double : " + Double.MAX_VALUE);	// 1.7976931348623157E308
	}

}
