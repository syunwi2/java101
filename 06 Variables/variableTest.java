package p01;

public class variableTest {

	public static void main(String[] args) {
		// i. Declare Variable
		int n;			// 4byte
		String name;	// 4byte

		// ii. Initialize Variable
		n = 10;			// data in n = 10;
		name = "Kim";	// data in name = address of "Kim";
		
		System.out.println(n);
		System.out.println(name);
		
		// iii. Storing very big number
		long x = 1234512345L;	// L
	}

}
